thistuple = (1, 3, 7, 8, 7, 5, 4, 6, 8, 5)

# Returns the number of times a specified value occurs in a tuple
x = thistuple.count(5)
print("Number of occurrences of 5:", x)

# Searches the tuple for a specified value and returns the position of where it was found -> return first occurrence of the value
y = thistuple.index(7)
print("Position of first occurrence of 7:", y)