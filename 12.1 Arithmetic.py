"""
+	    Addition            x + y	
-	    Subtraction         x - y	
*	    Multiplication	    x * y	
/	    Division	        x / y	
%   	Modulus	            x % y	
**	    Exponentiation      x ** y	
//	    Floor division      x // y

"""

x = 10
y = 5

add = x + y
sub = x - y
mul = x * y
div = x / y
mod = x % y
exp = x ** y
floor_div = x // y

print("Addition: ", add)
print("Substraction: ", sub)
print("Multiplication: ", mul)
print("Division: ", div)
print("Modulus: ", mod)
print("Exponentiation: ", exp)
print("Floor Division: ", floor_div)