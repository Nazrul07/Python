# Single quote
print('It\'s a sunny day!')

# Backslash
print("This is a backslash: \\")

# New Line
print("Hello\nWorld")

# Carriage Return -> Overwrite
print("Hello\rWorld")
print("What's up\rHow are you doing?")
print("Welcome, Learn Python\rHurryUp")

# Tab
print("Name:\tNazrul")

# Backspace
print("Helloo\b!")

# Octal Value
print("\110\145\154\154\157")  # Octal for 'Hello'

# Hexadecimal Value
print("\x48\x65\x6c\x6c\x6f")  # Hex for 'Hello'