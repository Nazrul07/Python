# Join Tuples
tuple1 = ("a", "b" , "c")
tuple2 = (1, 2, 3)

tuple3 = tuple1 + tuple2
print(tuple3)
print()

# Multiply Tuples
fruits = ("apple", "banana", "cherry")
mytuple = fruits * 2
print(mytuple)

num_tuple = (1, 2, 3)
new_tuple = num_tuple * 3   # It not that the numbers are multiplied, but the tuple is repeated that many times.
print(new_tuple)
print()

